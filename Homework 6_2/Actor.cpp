	Actor::Actor()
	{
		name_ = "";
	}
	Actor::Actor(string name)
	{
		name_ = name;
	}
	//------------------------------------------------------
	//------- Destructor -----------------------------------
	//------------------------------------------------------

	Actor::~Actor()
	{
		movieList_.clear();
	}

	//------------------------------------------------------
	//------- Inspectors -----------------------------------
	//------------------------------------------------------

	string Actor::getName() const
	{
		return name_;
	}

	int Actor::setNumberOfMovies(int x) const
	{
		return x;
	}

	int Actor::getNumberOfMovies() const
	{
		return numMovies_;
	}

	string Actor::getMovieName(int i) const
	{
		return movieList_[i]->getName();
	}

	Movie *Actor::getMovie(int i) const
	{
		return movieList_[i];
	}

	void Actor::addMovie(Movie *p)
	{
		numMovies_++;
		movieList_.push_back(p);
	}



